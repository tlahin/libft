#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*str;

	i = 0;
	str = (void *)s;
	while (i < n)
	{
		if (((unsigned char *)str)[i] == ((unsigned char)c))
			return ((void *)&str[i]);
		i++;
	}
	return (NULL);
}
